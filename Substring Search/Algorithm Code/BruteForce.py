import time

#Brute Force Algorithm
#====================================================================================================================================================================================
#====================================================================================================================================================================================


def naive(string, pattern):
    
    #Tracking the execution time
    start_time = time.time()
    
    ls = len(string)
    lp = len(pattern)
    maxComp = (ls-lp+1)
    res = []

    numLoops = 0
    for i in range(maxComp):
        numLoops += 1
        j = 0
        while (j < lp):
            numLoops += 1
            if (pattern[j] != string[j+i]):
                break
            j += 1
        if j == lp:
            res.append(i)
            
    print("\n\nNumber of matched found : ", len(res))
    print("--- %s seconds ---" % (time.time() - start_time))

